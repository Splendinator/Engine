#include "Mesh.h"



Mesh::Mesh()
{
	verts = nullptr;
	cols = nullptr;
}


Mesh::~Mesh()
{
	delete [] verts;
}

void Mesh::buffer() const{
	GLuint bufferNum;
	glGenBuffers(1, &bufferNum);
	glBindBuffer(GL_ARRAY_BUFFER, bufferNum);
	glBufferData(GL_ARRAY_BUFFER, num * sizeof(GLfloat), verts, GL_STATIC_DRAW);
}

Mesh * Mesh::Triangle() {
	Mesh *m = new Mesh;
	m->num = 9;
	m->verts = new GLfloat[m->num]{ -1,-1,0,0,1,0,1,-1,0 };
	
	return m;
}

